int	set_ord(char *str)
{
	int	i;
	int	num;

	num = 0;
	i = 0;
	while (str[i] && num >= 0)
	{
		if ((str[i] == ' ' && !(str[i + 1] >= '1' && str[i + 1] <= '9'))
			|| (str[i] >= '1' && str[i] <= '9' && 
				str[i + 1] != '\0' && str[i + 1] != ' ')
			|| (str[i] != ' ' && !(str[i] >= '1' && str[i] <= '9')))
			num = -1;
		else if (str[i] >= '1' && str[i] <= '9')
			num++;
		i++;
	}
	i = 1;
	while (i * i != num && i <= 9)
		i++;
	if (i == 10)
		return (-1);
	return (i);
}
