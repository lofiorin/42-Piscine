int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	flag;

	i = 0;
	flag = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 97 || 122 < str[i])
			flag = 0;
		i++;
	}
	return (flag);
}
